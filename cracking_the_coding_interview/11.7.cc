#include <iostream>
#include <vector>
using namespace std;
class Stack {
public:
    int getHeight(vector<int> men, int n) {
        vector<int> cnt(n, 1);
        for (int i = 0; i < n; ++i)
        {
            for (int j = i - 1; j >= 0; --j)
            {
                if (men[j] < men[i])
                    cnt[i] = max(cnt[i], cnt[j] + 1);
            }
        }
        int rnt = 0;
        for (int i = 0; i < n; ++i)
            rnt = max(rnt, cnt[i]);
        return rnt;
    }
};










